/*财务处的小胡老师最近就在考虑一个问题：如果每个老师的工资额都知道，最少需要准备多少张人民币，
才能在给每位老师发工资的时候都不用老师找零呢？
这里假设老师的工资都是正整数，单位元，人民币一共有100元、50元、10元、5元、2元和1元六种。*/
#include <stdio.h>
int main (void)
{
	int n;
	scanf("%d",&n);
	while( n ){
		int sum = 0;
		int i,j;
		for( i = 0 ; i < n ; i++ ){
			scanf("%d",&j);
			sum += j / 100 + ( j % 100 ) / 50 + ( j % 50 ) / 10 + ( j % 10 ) / 5 + ( j % 5 ) / 2 + ( j % 2 ) / 1;
		}
		printf("%d\n",sum);
	}
	return 0;
 } 
